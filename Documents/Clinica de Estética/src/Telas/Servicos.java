    private void limparCampos(){
        //this.CampoID.setText("");
        this.CampoServico.setText("");
        }
    
    private void limparTabela(){
        while(TabelaServicos.getRowCount() > 0){
            DefaultTableModel dm = (DefaultTableModel) TabelaServicos.getModel();
            dm.getDataVector().removeAllElements();
        }
    }
    
    private void atualizaTabela(ServicosDao cadastroPDao){
        try{
                    limparTabela();

                    ArrayList<ServicosModel> listaCadastros;
                    listaCadastros = cadastroPDao.consultar();        
                    DefaultTableModel modeloTabela = (DefaultTableModel) TabelaServicos.getModel();

                    for(ServicosModel cadastroP : listaCadastros){
                        modeloTabela.addRow(new String[]{Integer.toString(cadastroP.getID()),cadastroP.getServico()});
                    }

                }
                catch(Exception ex){
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado na linha 275:\n" + ex.getMessage(), "ERRO!", ERROR_MESSAGE);
                }
     
    }
